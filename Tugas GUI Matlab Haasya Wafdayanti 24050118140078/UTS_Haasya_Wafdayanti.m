function pushbutton5_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton5 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global data
[n m]=size (data);
y=data(:,1);
x=data(:,2:m);
reg=regstats(y,x);
res_ols=regstats(y,x);
set(handles.edit15,'string',res_ols.mse);
set(handles.edit16,'string',res_ols.rsquare);
BETA0=reg.beta(1,1);
BETA1=reg.beta(2,1);
set (handles.edit1,'string',BETA0);
set (handles.edit4,'string',BETA1);
set(handles.edit9,'string',res_ols.fstat.f);
set(handles.edit12,'string',res_ols.fstat.pval);
set(handles.edit11,'string',res_ols.dwstat.dw);
set(handles.edit14,'string',res_ols.dwstat.pval);
set (handles.edit10,'string',reg.tstat.t(2,1));
set (handles.edit13,'string',reg.tstat.pval(2,1));
x1=min(x):0.01:max(x);
y1=BETA0+BETA1*x1;

axes(handles.axes1);
    ylim([min(y)-1 max(y)+1]);
    xlim([min(x)-1 max(x)+1]);
plot(x1,y1,'b-',x,y,'ko')

